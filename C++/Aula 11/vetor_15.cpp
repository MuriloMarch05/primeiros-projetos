/*
1) Escreva um programa que armazene em um vetor todos os números inteiros de 1 a 15. Após
isso, o programa deve imprimir todos os valores armazenados.
*/
#include <iostream>
using namespace std;

int main()
{
	int vet[15], i;
	
	for(i=1;i<=15;i++)
	{
		cout << vet[15] << " ";
	}
	
	return 0;	
}
